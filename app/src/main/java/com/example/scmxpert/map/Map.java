    public boolean checkLocationPermission() {
        if (ContextCompat.checkSelfPermission(this,
                Manifest.permission.ACCESS_FINE_LOCATION)
                != PackageManager.PERMISSION_GRANTED) {

            if (ActivityCompat.shouldShowRequestPermissionRationale(this,
                    Manifest.permission.ACCESS_FINE_LOCATION)) {
                ActivityCompat.requestPermissions(this,
                        new String[]{Manifest.permission.ACCESS_FINE_LOCATION},
                        MY_PERMISSIONS_REQUEST_LOCATION);
            } else {
                ActivityCompat.requestPermissions(this,
                        new String[]{Manifest.permission.ACCESS_FINE_LOCATION},
                        MY_PERMISSIONS_REQUEST_LOCATION);
            }
            return false;
        } else {
            return true;
        }
    }
    @Override
    public void onRequestPermissionsResult(int requestCode,
                                           String permissions[], int[] grantResults) {
        switch (requestCode) {
            case MY_PERMISSIONS_REQUEST_LOCATION: {
                if (grantResults.length > 0
                        && grantResults[0] == PackageManager.PERMISSION_GRANTED) {
                    if (ContextCompat.checkSelfPermission(this,
                            Manifest.permission.ACCESS_FINE_LOCATION)
                            == PackageManager.PERMISSION_GRANTED) {
                        if (mGoogleApiClient == null) {
                            buildGoogleApiClient();
                        }
                        mMap.setMyLocationEnabled(true);
                    }
                } else {
                    Toast.makeText(this, "permission denied",
                            Toast.LENGTH_LONG).show();
                }
                return;
            }
        }
    }

    protected void createMarker() {
        if (getIntent() != null) {
            Bundle b = getIntent().getExtras();
//getting the arraylist from the key
            ArrayList<Object> wave_list = (ArrayList<Object>) b.getSerializable(ApiConstants.WAVE_POINT);

            for (int i = 0; i < wave_list.size(); i++) {
                object = wave_list.get(i);

                for (int k = 0; k < ((List) object).size(); k++) {
                    ArrayList<Object> list = (ArrayList<Object>) ((List) object).get(k);

                    for (int j = 0; j < list.size(); j++) {
                        if (j == 0) {
                            lat = (String) list.get(j);
                        } else {
                            longt = (String) list.get(j);
                        }
                    }
                    double latitude1 = Double.parseDouble(lat);
                    double longitude1 = Double.parseDouble(longt);
                    // createMarker(latitude,longitude);
                    mMap.addMarker(new MarkerOptions()
                            .position(new LatLng(latitude1, longitude1))
                            .anchor(0.5f, 0.5f));


                }
            }


        }
    }

